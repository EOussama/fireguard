/**
 * @description
 * Enum representing different pages in the application.
 */
export enum Page {

  /**
   * @description
   * Represents the index page.
   */
  Index = '/',

  /**
   * @description
   * Represents the success page.
   */
  Success = '/success',

  /**
   * @description
   * Represents the failure page.
   */
  Failure = '/failure'
}